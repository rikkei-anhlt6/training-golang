package main

import (
	"fmt"
)

func main() {
	var a int
	fmt.Printf("%d\n", a) //default value kiểu int = 0
	var b float64
	fmt.Printf("%f\n", b) //default value kiểu float = 0.0
	var c bool
	fmt.Printf("%t\n", c) //default value kiểu bool là false
	var d string
	fmt.Printf("%s\n", d) //default value kiểu string là ""
}
